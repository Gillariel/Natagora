package com.helmo.al.natarest.viewmodel;

import com.helmo.al.natarest.entity.Bird;

/**
 *
 * @author e140577
 */
public class BirdCircular {

    private int id;
    private String name;
    private int length;
    private int weight;
    
    public BirdCircular(Bird b) {
        this.id = b.getId();
        this.name = b.getName();
        this.length = b.getLength();
        this.weight = b.getWeight();
    }

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the length
     */
    public int getLength() {
        return length;
    }

    /**
     * @param length the length to set
     */
    public void setLength(int length) {
        this.length = length;
    }

    /**
     * @return the weight
     */
    public int getWeight() {
        return weight;
    }

    /**
     * @param weight the weight to set
     */
    public void setWeight(int weight) {
        this.weight = weight;
    }
    
    
}
